package models.objects;

public class Products {
    private int ID;
    private String Name;
    private int Price;

    public int getID() {
        return ID;
    }

    public void setID(int id) {
        ID = id;
    }

    public String getName() {
        return Name;
    }

    public void setName(String N) {
        Name = N;
    }

    public int getPrice() {
        return Price;
    }

    public void setPrice(int P) {
        Price = P;
    }
}
